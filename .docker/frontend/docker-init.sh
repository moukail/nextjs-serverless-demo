#!/usr/bin/env bash

# create new project if not exists
if [ ! -d "./src" ]; then
  . /home/install.sh
fi

echo "-------------------------------------------------------------------"
echo "-                              init                               -"
echo "-------------------------------------------------------------------"

yarn install

echo "-------------------------------------------------------------------"
echo "-                              build                              -"
echo "-------------------------------------------------------------------"

yarn build

echo "-------------------------------------------------------------------"
echo "-                        website is ready                         -"
echo "-------------------------------------------------------------------"

yarn start

tail -f /dev/null